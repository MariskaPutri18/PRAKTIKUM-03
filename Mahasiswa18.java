 class Mahasiswa18 {
    
        public String nama;
        public String nim;
        public char jenisKelamin;
        public double ipk;
    
        public Mahasiswa18 (String nama, String nim, char jenisKelamin, double ipk) {
            this.nama = nama;
            this.nim = nim;
            this.jenisKelamin = jenisKelamin;
            this.ipk = ipk;
        }
    
        public String getNama() {
            return nama;
        }
    
        public String getNim() {
            return nim;
        }
    
        public char getJenisKelamin() {
            return jenisKelamin;
        }
    
        public double getIpk() {
            return ipk;
        }
    
}
